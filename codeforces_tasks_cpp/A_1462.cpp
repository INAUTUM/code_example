#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t;
    cin >> t;
    ll n, a, b;
    vector<ll> mas;
    while(--t >= 0){
        cin >> n;
        mas.clear();
        mas.resize(n);
        for(auto &x : mas) cin >> x;
        a = 0, b = n - 1;
        while(a < b){
            cout << mas[a] << " " << mas[b] << " ";
            ++a, --b;
        }
        n % 2 == 1 ? cout << mas[a] << endl : cout << endl;
    }
}
