using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n;
    for(cin >> t; cin >> n; cout << endl){
        vector<ll> a(n);
        vector<ll> b(n);
        ll x = 0, l = 0;
        bool f = true;
        for(auto &a : a) cin >> a;
        if(l < 0) f = false;
        for(ll i = 0; i < n; ++i){
            cin >> b[i];
            l = max(l, a[i] - b[i]);
        }
        for(ll i = 0; i < n && f; ++i){
            if(a[i] - b[i] == l || b[i] == 0) continue;
            f = false;
        }
        cout << (f ? "YES" : "NO");
    }
}
