#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, k, a;
    string s;
    cin >> t;
    while(--t >= 0){
        cin >> s;
        k = 1, a = 1;
        sort(s.begin(), s.end());
        for(ll i = 1; i < s.size(); ++i){
            if(s[i] == s[i - 1] && k == 1) {++k; ++a;}
            else if(s[i] != s[i - 1]){
                k = 1;  
                ++a;
            }
            else continue;
        }
        cout << a / 2 << endl;
    }
}
