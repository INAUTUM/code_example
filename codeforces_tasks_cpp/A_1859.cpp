using namespace std;

int main(){
    ll t; cin >> t;
    while(t--){
        ll n, i; cin >> n;
        vector<ll> a(n);
        for(auto &a : a) cin >> a;
        sort(a.begin(), a.end());
        bool f = true;
        for(i = 0; i < n - 1 && f; ++i){
            if(a[i] != a[i + 1]) f = false;
        }
        if(f) cout << -1 << endl;
        else{
            cout << i << " " << n - i << endl;
            for(ll j = 0; j < i; ++j) cout << a[j] << " ";
            cout << endl;
            for(ll j = i; j < n; ++j) cout << a[j] << " ";
            cout << endl;
        }
    }
}