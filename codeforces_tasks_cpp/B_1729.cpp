using namespace std;

int main(){
    ll t, n;
    string s, ans;
    cin >> t;
    while(-- t >= 0){
        cin >> n >> s;
        ans = "";
        for(ll i = n - 1; i >= 0; --i){
            if(s[i] == '0') {ans = char('a' + (s[i - 2] - '0') * 10 + (s[i - 1] - '0') - 1) + ans; i -= 2;}
            else  ans = char('a' + (s[i] - '0') - 1) + ans;
        }
        cout << ans << endl;
    }
}
