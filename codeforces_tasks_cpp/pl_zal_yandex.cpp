#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long
#define dd double
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll n, m; cin >> n >> m;
    ll ans = 0;
    for(ll i = 0; i < n; ++i){
        string s; cin >> s;
        for(ll j = 0; j < m; ++j){
            if(s[j] == '/' && j + 1 < m && s[j + 1] == '\\') ++ans;
            else if(s[j] == '\\' && j + 1 < m && s[j + 1] == '/') ++ans;
            else if(s[j] == '/' && (j - 1 > 0 && s[j - 1] == '.' && j + 1 < m && s[j + 1] == '.' || j + 1 == m && j - 1 > 0 || s[j - 1] == '.')) ++ans;
            else if(s[j] == '\\' && (j - 1 > 0 && s[j - 1] == '.' && j + 1 < m && s[j + 1] == '.')) ++ans;
        }
    }
    cout << ans << endl;
}