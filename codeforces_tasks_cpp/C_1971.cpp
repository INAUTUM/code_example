int main(){
    ll t;
    cin >> t;
    string s;
    ll a, b, n, ans;
    while(--t >= 0){
        cin >> n;
        cin >> s;
        a = 0;
        b = n - 1;
        ans = 0;
        while(true){
            if(s[a] == '1' && s[b] == '0' || s[a] == '0' && s[b] == '1'){
                n -= 2;
                ++a, --b;
            }
            if(a >= b || s[a] == s[b]) break;
        }
        cout << n << endl;
    }
}
