#define vi vector <long long>
#define forn(i, uoi) for(ll i = 0; i < uoi; i++)
#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long
#define dd double
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll n, m, q;
    cin >> n >> m >> q;
    vector<ll> a(1000001);
    vector<ll> b(1000001);
    for(ll i = 0; i < n; ++i){
        ll x; cin >> x;
        ++a[x];
    }
    for(ll i = 0; i < m; ++i){
        ll x; cin >> x;
        ++b[x];
    }
    while(--q >= 0){
        ll ans = 0;
        ll t, c;
        char p;
        cin >> t >> p >> c;
        if(p == 'A'){
            if(t == 1) ++a[c];
            else --a[c];
        }
        else{
            if(t == 1) ++b[c];
            else --b[c];
        }
        for(ll i = 0; i < 1000001; ++i){
            ans += abs(a[i] - b[i]);
        }
        cout << ans << " ";
    }
    cout << endl;
}