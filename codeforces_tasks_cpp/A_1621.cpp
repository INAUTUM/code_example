int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n, k;
    for(cin >> t; cin >> n >> k;){
        if((n + 1) / 2 >= k){
            ll u = 0;
            for(ll i = 0; i < n; ++i){
                for(ll j = 0; j < n; ++j){
                    if(j == u && i == u && u / 2 != k) {cout << 'R'; u += 2;}
                    else cout << '.';
                }
                cout << endl;
            }
        }
        else cout << -1 << endl;
    }
}
