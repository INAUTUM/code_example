#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, ans, y, k;
    string s;
    cin >> t;
    while(--t >= 0){
        cin >> s;
        ans = 0, k = 0;
        y = s.size();
        for(ll i = 0; i < s.size(); ++i){
            s[i] == '0' && y != s.size()  ? ++k : s[i] == '1' ? y = i, ans += k, k = 0 : 0;
        }
        cout << ans << endl;
    }
}
