#define vi vector <long long>
#define forn(i, uoi) for(ll i = 0; i < uoi; i++)
#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, n, k, j;
    vector<int> a;
    string h;
    cin >> t;
    while(--t >= 0){
        cin >> n;
        a.clear();
        a.resize(n);
        for(auto &a : a) cin >> a;
        j = 0;
        for(ll i = 0; i < n; ++i){
            cin >> k >> h;
            for(auto l : h){
                if(l == 'D') {a[j] + 1 == 10 ? a[j] = 0 : ++a[j];}
                else {a[j] - 1 == -1 ? a[j] = 9 : --a[j];}
            }
            ++j;
        }
        for(auto a : a) cout << a << " ";
        cout << endl;
    }
}
