using namespace std;

int main(){
    ll t, n, l, r, mid;
    cin >> t;
    while(--t >= 0){
        cin >> n;
        l = 0, r = 10e9;
        while(r - l > 1){
            mid = l + (r - l) / 2;
            if(mid * mid >= n) r = mid;
            else l = mid;
        }
        cout << r - 1 << endl;
    }
}