int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t;
    string n;
    for(cin >> t; cin >> n; cout << endl){
        ll j = 25, ans = 0;
        bool f = true;
        for(ll i = 'a'; i <= 'z' && f; ++i){
            if(i == n[0]) {f = false; continue;}
            ans += j;
        }
        f = true;
        for(ll i = 'a'; i <= 'z' && f; ++i){
            if(n[0] == i) continue;
            else if(n[1] == i) f = false;
            ++ans;
        } 
        cout << ans;
    }
}