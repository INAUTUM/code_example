#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, n, ans, k;
    vector<ll> mas;
    cin >> t;
    while(--t >= 0){
        ans = 0, k = 0;
        cin >> n;
        mas.clear();
        mas.resize(n);
        for(auto &a : mas) {cin >> a; k += a;}
        if(k % n != 0) cout << -1 << endl;
        else{
            k /= n;
            for(auto a : mas) if(a > k) ans += 1;
            cout << ans << endl;
        }
    }
}
