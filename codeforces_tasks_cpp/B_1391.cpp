#define vi vector <long long>
#define ll long long
#define dd double
#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t;
    for(cin >> t; t--; cout << endl){
        ll a, b, ans = 0; cin >> a >> b;
        for(ll i = 1; i <= a; ++i){
            for(ll j = 1; j <= b; ++j){
                char x; cin >> x;
                if(x == 'C') continue;
                else if(i == a && x == 'D') ++ans;
                else if(j == b && x == 'R') ++ans;
            }
        }
        cout << ans;
    }
}