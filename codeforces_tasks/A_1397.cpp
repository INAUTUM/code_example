#define vi vector <long long>
#define forn(i, uoi) for(ll i = 0; i < uoi; i++)
#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, n;
    bool f;
    string s;
    vector<ll> a(26);
    cin >> t;
    while(--t >= 0){
        cin >> n;
        a.clear();
        a.resize(26);
        f = true;
        for(ll i = 0; i < n; ++i){
            cin >> s;
            for(auto c : s) ++a[c - 'a'];
        }
        for(auto a : a){
            if(a % n > 0){
                f = false;
                break;
            }
        }
        cout << (f ? "YES" : "NO") << endl;
    }
}
