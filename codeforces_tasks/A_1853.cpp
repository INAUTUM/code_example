    cin.tie(0), cout.tie(0);
    ll t, n;
    for(cin >> t; cin >> n; cout << endl){
        ll ans = 1e9, x, a;
        cin >> a;
        x = a;
        --n;
        while(n--){
            cin >> a;
            ans = min(ans, a - x);
            x = a;
        }
        cout << max((ll) 0, (ans + 2) / 2);
    }
