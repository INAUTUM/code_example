int main(){
    ll n, i = 1, ans = 1e9, ind1, ind2; cin >> n;
    vector<pair<ll, ll>> a(n);
    for(auto &a : a){
        ll b; cin >> b;
        a = make_pair(b, i);
        ++i;
    }
    sort(a.begin(), a.end());
    for(i = 0; i < n - 1; ++i){
        if(ans > a[i + 1].first - a[i].first){
            ans = a[i + 1].first - a[i].first;
            ind1 = a[i].second; ind2 = a[i + 1].second;
        }
    }
    cout << ind1 << " " << ind2 << endl;
}