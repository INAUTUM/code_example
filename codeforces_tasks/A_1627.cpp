#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n, m, r, c;
    for(cin >> t; cin >> n >> m >> r >> c; cout << endl){
        bool f = false;
        short ans = 2;
        string s;
        for(ll i = 1; i <= n; ++i){
            cin >> s;
            for(ll j = 0; j < m; ++j){
                if(!f && s[j] == 'B') f = true;
                s[j] == 'B' && i == r && j + 1 == c ? ans = 0 : s[j] == 'B' && ans == 2 && (i == r || j + 1 == c) ? ans = 1 : 0;
            }
        }
        cout << (f ? ans : -1);
    }
}
