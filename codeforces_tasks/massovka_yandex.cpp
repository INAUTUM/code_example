#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long
#define dd double
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll n, m, ans = 0; cin >> n >> m;
    vector<ll> a(n, 0);
    for(ll i = 0; i < m; ++i){
        ll l, r, x; cin >> l >> r >> x;
        for(ll j = l - 1; j < r; ++j){
            a[j] < x ? a[j] = x : 0;
        }
    }
    for(auto a : a) ans += a;
    cout << ans << endl;
}