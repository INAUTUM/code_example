#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n, x;
    for(cin >> t; cin >> n >> x; cout << endl){
        vector<ll> a(2 * n);
        for(auto &a : a) cin >> a;
        sort(a.begin(), a.end());
        bool f = true;
        for(ll i = 0; i < n && f; ++i) if(a[i + n] - a[i] < x) f = false;
        cout << (f ? "YES" : "NO");
    }
}