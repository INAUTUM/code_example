using namespace std;

int main(){
    ll t, n, k, l, r, mid, a;
    vector<int> mas;
    bool f;
    for(ll i = 2; i <= 30000; ++i){
        t = true;
        for(ll j = 2; j <= sqrt(i); ++j){
            if(i % j == 0) {t = false; break;}
        }
        if(t) mas.push_back(i);
    }
    cin >> t;
    while(--t >= 0){
        cin >> n;
        k = 1 + n;
        l = 0, r = mas.size() - 1;
        for(ll i = 0; i < sqrt(mas.size()) + 1; ++i){
            mid = l + (r - l) / 2;
            mas[mid] >= k ? r = mid : l = mid;
        }
        a = mas[r];
        l = r;
        r = mas.size() - 1;
        k = a + n;
        for(ll i = 0; i < sqrt(mas.size()) + 1; ++i){
            mid = l + (r - l) / 2;
            mas[mid] >= k ? r = mid : l = mid;
        }
        cout << a * mas[r] << endl;
    }
}

