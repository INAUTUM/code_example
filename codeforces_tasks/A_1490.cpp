#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, n, ans, ax, b;
    vector<ll> a;
    cin >> t;
    while(--t >= 0){
        ans = 0;
        cin >> n;
        a.clear();
        a.resize(n);
        for(auto &a : a) cin >> a;
        for(ll i = 0; i < n - 1; ++i){
            ax = max(a[i], a[i + 1]);
            b = min(a[i], a[i + 1]);
            if(b * 2 >= ax) continue;
            else{
                while(b * 2 < ax) {++ans; ax = ceil((double) ax / 2);}
            }
        }
        cout << ans << endl;
    }
}
