using namespace std;

int main(){
    ll t;
    cin >> t;
    string s;
    ll x, y, i, n;
    while(--t >= 0){
        cin >> n;
        cin >> s;
        x = 0, y = 0, i = 0;
        while(i < n){
            s[i] == 'U' ? ++y : (s[i] == 'D' ? --y : (s[i] == 'L'? --x : ++x));
            ++i;
            if(x == 1 && y == 1) break;
        }
        cout << (x == 1 && y == 1 ? "YES" : "NO") << endl;
    }
}
