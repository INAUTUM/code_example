using namespace std;

using namespace std;

int main(){
    ll t; cin >> t;
    while(--t >= 0){
        ll n, m; cin >> n >> m;
        vector<ll> a(n);
        vector<ll> b(m);
        bool f = 0;
        ll ans;
        for(auto &a : a) cin >> a;
        for(auto &b : b) cin >> b;
        sort(a.begin(), a.end());
        sort(b.begin(), b.end());
        for(ll i = 0; i < n; ++i){
            for(ll j = 0; j < m; ++j){
                if(a[i] == b[j]) {ans = i; f = 1; break;}
                else if(a[i] < b[j]) break;
            }
            if(f) break;
        }
        if(f){
            cout << "YES" << endl << 1 << " " << a[ans] << endl;
        }
        else cout << "NO" << endl;
    }
}