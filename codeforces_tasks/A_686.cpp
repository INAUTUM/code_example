#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll n, x, j, ans = 0;;
    cin >> n >> x;
    char k;
    while(--n >= 0){
        cin >> k >> j;
        if(k == '+') x += j;
        else if(k == '-' && x >= j) x -= j;
        else ++ans;
    }
    cout << x << " " << ans << endl;
}
