#define vi vector <long long>
#define ll long long
#define dd double
#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n;
    for(cin >> t; cin >> n; cout << endl){
        ll ans = 0, ans1 = 0;
        bool f1 = true, f2 = true, f3 = true;
        vector<pair<string, ll>> a(n);
        for(auto &a : a) cin >> a.second >> a.first;
        sort(a.begin(), a.end());
        for(auto a : a){
            if(a.first == "01" && f1) {ans += a.second; f1 = false; continue;}
            else if(a.first == "10" && f2) {ans += a.second; f2 = false; continue;}
            else if(a.first == "11" && f3) {ans1 = a.second; f3 = false; continue;}
        }
        if(!(f1 || f2 || f3)) cout << min(ans, ans1);
        else if(!(f1 || f2)) cout << ans;
        else if(!f3) cout << ans1;
        else cout << -1;
    }
}