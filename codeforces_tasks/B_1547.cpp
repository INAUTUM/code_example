using namespace std;

int main(){
    ll t;
    string s;
    for(cin >> t; cin >> s, --t >= 0; cout << endl){
        ll i, j, c; 
        i = j = s.find('a');
        c = s[i];
        if(i == string::npos) goto exit;
        while(i + 1 < s.size() || j - 1 >= 0){
            if(i + 1 < s.size() && s[i + 1] == c + 1) ++i, c = s[i];
            else if(j - 1 >= 0 && s[j - 1] == c + 1) --j, c = s[j];
            else goto exit;
        }
        cout << "YES"; 
        continue;
        exit: cout << "NO";
    }
}