#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long
#define dd double
#include <bits/stdc++.h>

using namespace std;

using namespace std;

int main(){
    ll n, m, ans = 0;
    cin >> n >> m;
    ll a[1001][1001], b[1001][1001], c[1001][1001];
    for(ll i = 1; i <= n; ++i){
        for(ll j = 1; j <= m; ++j){
            cin >> a[i][j];
        }
    }
    for(ll i = 1; i <= n; ++i){
        for(ll j = 1; j <= m; ++j){
            b[i][j] = b[i][j - 1] + a[i][j];
            c[i][j] = c[i - 1][j] + a[i][j];
        }
    }
    for(ll i = 1; i <= n; ++i){
        for(ll j = 1; j <= m; ++j){
            if(!a[i][j]){
                if(b[i][j - 1]) ++ans;
                if(b[i][m] - b[i][j]) ++ans;
                if(c[i - 1][j]) ++ans;
                if(c[n][j] - c[i][j]) ++ans;
            }
        }
    }
    cout << ans << endl;
}