#define vi vector <long long>
#define ll long long
#define dd double
#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n;
    for(cin >> t; cin >> n;){
        vector<string> s(n);
        ll a = 0, b = 0, c = 0, d = 0, i = 0;
        bool f = true, g = false;
        for(auto &s : s){
            cin >> s;
            for(ll j = 0; j < n; ++j){
                if(s[j] == '*'){a = i; b = j; g = true;}
                if(f == true && g == true){c = a; d = b; f = false;}
            }
            // cout << a << ' ' << b << ' ' << c << ' ' << d << endl;
            ++i; 
        }
        // cout << a << ' ' << b << ' ' << c << ' ' << d << endl;
        if(a == c || b == d){
            a == c ? a < n - 1 ? ++a : --a : 0;
            b == d ? b < n - 1 ? ++b : --b : 0;
        }
        // cout << a << " " << b << ' ' << c << ' ' << d << endl;
        for(i = 0; i < n; ++i){
            for(ll j = 0; j < n; ++j){
                if(a == i && d == j || c == i && b == j || a == i && b == j || c == i && d == j) cout << '*';
                else cout << '.';
            }
            cout << endl;
        }
    }
}