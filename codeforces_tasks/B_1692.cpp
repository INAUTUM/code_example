#define foran(i, a, n) for(int i = a; i < n; i++)
#define ll long long 
#include <bits/stdc++.h>

using namespace std;

int main(){
    ll t, n, ax, b, ans;
    vector<ll> a(10001);
    cin >> t;
    while(--t >= 0){
        cin >> n;
        b = 0;
        ans = 0;
        a.clear();
        a.resize(10001);
        for(ll i = 0; i < n; ++i){
            cin >> ax;
            ++a[ax];
        }
        for(ll i = 0; i < 10001; ++i){
            if(a[i] % 2 == 1) ++ans;
            else{
                a[i] != 0 ? ++b : 0;
            }
        }
        cout << ans + b - b % 2 << endl;
    }
}
