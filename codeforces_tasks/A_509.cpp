using namespace std;

int main(){
    ll n;
    cin >> n;
    vector <ll> a(n, 1);
    vector <ll> b(n, 1);
    for(ll i = 0; i < n - 1; i++){
        b[0] = a[0];
        for(ll j = 1; j < n; j++){
            b[j] = b[j - 1] + a[j];
        }
        a = b;
    }
    cout << b[n - 1] << endl;
}
