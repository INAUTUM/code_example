#include <bits/stdc++.h>
using namespace std;

int main(){ 
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll n, x, l = 0, ans = 0;
    cin >> n;
    vector<ll> a(n);
    cin >> x;
    ++l;
    for(ll i = 1; i < n; ++i){
        cin >> x;
        if(x == 1) a[ans] = l, l = 1, ++ans;
        else ++l;
    }
    a[ans] = l;
    cout << ans + 1 << endl;
    for(ll i = 0; i < n; ++i){
        if(a[i] == 0) break;
        cout << a[i] << ' ';
    }
    cout << endl;
}
