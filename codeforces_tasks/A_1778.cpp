#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n;
    for(cin >> t; cin >> n; cout << endl){
        ll ans = 0;
        bool f = true, b = true;
        ll x1; cin >> x1;
        ans = x1;
        for(ll i = 1; i < n; ++i){
            ll x; cin >> x;
            ans += x;
            if(x == x1 && x1 == -1 && f){ans += 4; f = false;}
            else if(x1 == -1 || x == -1 && b) b = false;
            x1 = x;
        }
        cout << (!f ? ans : !b ? ans : ans - 4);
    }
}
