#define vi vector <long long>
#define ll long long
#define dd double
#include <bits/stdc++.h>
using namespace std;

int main(){ 
    ios::sync_with_stdio(false);
    cin.tie(0), cout.tie(0);
    ll t, n, m;
    for(cin >> t; cin >> n >> m; cout << endl){
        vector<char> s(m, 'B');
        for(ll i = 0; i < n; ++i){
            ll x; cin >> x;
            if(x - 1 < m - x && s[x - 1] == 'B') s[x - 1] = 'A';
            else s[m - x] == 'B' ? s[m - x] = 'A' : s[x - 1] = 'A';
        }
        for(auto s : s) cout << s;

    }
}
